function CIRCLESembeddingsJORGENSEN_CALC( trALPHA, trBETA )
{
    // x^2-(trALPHA * trBETA)x+t_a^2+t_b^2=0
    var k = ( trALPHA.mul( trBETA ) ).opposite();
    var j = ( trALPHA.pow( 2.0 ) ).add( trBETA.pow( 2.0 ) );
    // so that it turns into : lx^2 + kx + j = 0
    var _delta = k.pow( 2.0 ).sub( j.mul( 4.0 ) );
    CIRCLESembeddingsJORGENSEN_alphabeta_1 = ( k.opposite().add( _delta.sqrt() ) ).div( 2.0 );
    CIRCLESembeddingsJORGENSEN_alphabeta_2 = ( k.opposite().sub( _delta.sqrt() ) ).div( 2.0 );
}

