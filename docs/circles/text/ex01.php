<a name="ex01" ID="title">Example #1: Draw the disks generators manually</a><br><br>
    It's very easy to draw your own generator disks. Just follow these steps:<br><br>
    (<b>1</b>) Move the mouse pointer to the position where the center of your first disk will be fixed, then click the left mouse button.<br>
    Now (<b>2</b>) don't release the mouse button and drag the pointer elsewhere:
    you'll see a red segment displaying the diameter of your next-to-be disk.
		(<b>3</b>) Stretch the diameter as required, then release the button. There you go ! The generator disk is ready.<br>
		Repeat these steps for more disks.
